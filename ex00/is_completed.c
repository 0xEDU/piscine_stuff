#include <unistd.h>

int	is_complete(int a[4][4]);

int	is_complete(int a[4][4])
{
	int	i;
	int	j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			if (a[i][j] > 4 || a[i][j] < 1)
			{
				return (0);
			}
			j++;
		}
		i++;
	}
	return (1);
}
